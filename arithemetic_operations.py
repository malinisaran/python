# Store input numbers: 
Num1 = input('Enter The First Number:')
Num2 = input('Enter The Second Number:')

# Add two numbers  
sum = float(Num1) + float(Num2)

# Subtract two numbers 
sub = float(Num1) - float(Num2)

mul = float(Num1) * float(Num2)

div = float(Num1) / float(Num2)


# Display the sum  
print("Sum of 2Numbers: {0} and {1} is {2}".format(Num1,Num2,sum))

# Display the subtraction  
print("Subtration of 2Numbers:{0} and {1} is {2}".format(Num1,Num2,sub))

print("Multiple of 2Numbers:{0} and {1} is {2}".format(Num1,Num2,mul))

print("Divide of 2Numbers:{0} and {1} is {2}".format(Num1,Num2,div))


